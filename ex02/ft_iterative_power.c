#include <stdio.h>

int	ft_iterative_power(int nb, int power)
{
	int	res;

	res = 1;
	if (power < 0)
		return (0);
	while (power > 0)
	{
		printf("before - %d\n", nb);
		res *= nb;
		printf("after - %d\n", nb);
		power--;
	}
	return (res);
}

int	main(void)
{
	printf("%d", ft_iterative_power(2, 36));
	return (0);
}
