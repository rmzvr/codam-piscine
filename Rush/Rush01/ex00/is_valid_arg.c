int	is_int(char c);

int	is_valid_arg(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (i % 2 == 0 && i != 0)
		{
			if (!is_int(str[i]))
				return (0);
		}
		else if (i % 2 != 0)
		{
			if (str[i] != ' ')
				return (0);
		}
		i++;
	}
	return (1);
}
