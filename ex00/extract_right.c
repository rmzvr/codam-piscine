void	extract_right(char *str, char *temp)
{
	int	i;
	int	j;

	i = 24;
	j = 0;
	while (i <= 31 && str[i] != '\0')
	{
		if (str[i] > '0' && str[i] < '9')
		{
			temp[j] = str[i];
			j++;
		}
		i++;
	}
	temp[j] = '\0';
}
